export const DAYS: Day[] = RAW.map((d, index) => ({
  index, name: d.name, date: d.date, iso: d.iso,
  t: d.t, wx: d.wx, cls: d.cls, badge: d.badge,
}));

/** uid -> Block registry. uid is `d{day}b{index}` from the original layout. */
export const BLOCKS: Record<string, Block> = {};
RAW.forEach((d, di) =>
  d.blocks.forEach((b, bi) => {
    const uid = `d${di}b${bi}`;
    BLOCKS[uid] = { uid, time: b[0], act: b[1], tag: b[2], dur: parseDur(b[0]) };
  })
);

export function defaultLayout(): string[][] {
  return RAW.map((d, di) => d.blocks.map((_, bi) => `d${di}b${bi}`));
}
